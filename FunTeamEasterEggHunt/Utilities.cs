        public static string PromptPassword()
        {
            bool validPassword = false;
            string password = "";

            WriteInfoPrompt("Please enter a password: ");

            while (!validPassword)
            {
                var passwords = Password.GetPasswords(); 
                password = Console.ReadLine();

                validPassword = passwords.Contains(password);

                if (!validPassword)
                {
                    Console.Clear();
                    Title.DisplayCongrats();
                    WriteInfoPrompt("Password Incorrect! Please enter a password: ");
                }
            }

            Console.Clear();

            return password;
        }

        public static void WriteInfoPrompt(string s)
        {
